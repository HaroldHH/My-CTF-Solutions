import base64

flag = [0xd9,0xb2,0xb9,0xf4,0xe3,0xc7,0xda,0xec,0xe3,0xb3,0xd1,0xd2,0xc5,0xb1,0xf4,0xd9,0xd4,0xb1,0xc9,0xd4,0xc5,0xee,0xb9,0xc3,0xd1,0xd6,0xce,0xc6,0xc6,0xe8,0xd2,0xaf,0xd5,0xb0,0xe8,0xca,0xd2,0xec,0xd1,0xd2,0xc5,0xe8,0xe8,0xe4]

minus_128_flag = ""
for x in flag:
    minus_128_flag += chr(x-128)

base64_flag = base64.b64decode(minus_128_flag)

real_flag = ""
for x in base64_flag:
    real_flag += chr(x ^ 32)

print(real_flag)
